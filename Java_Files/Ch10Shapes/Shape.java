
/**
 * Shape - has a getArea() and getPerimeter() method
 * 
 * @author Kyle Luu
 * @version 1.0
 */
public interface Shape
{
    /**
     * getArea
     * 
     * @return        the area of the shape 
     */
    double getArea();
    
    /**
     * getPerimeter
     * 
     * @return        the perimeter of the shape
     */
    double getPerimeter();
}
